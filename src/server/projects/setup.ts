import { spawn } from "node:child_process";
import * as fs from "node:fs/promises";
import * as path from "node:path";
import { ensureProjectInternalToken } from "@/server/ai-tools/projectToken";
import { logger } from "@/server/logger";
import { allocateProjectProductionPort } from "@/server/ports/allocate";
import { resolveHostPath } from "./hostPaths";
import {
	getProjectPath,
	getProjectPreviewPath,
	getProjectProductionPath,
	getProjectsPath,
	getTemplatePath,
} from "./paths";

export interface SetupProjectFilesystemResult {
	projectPath: string;
	productionPort: number;
}

export async function setupProjectFilesystem(
	projectId: string,
	devPort: number,
): Promise<SetupProjectFilesystemResult> {
	const projectPath = getProjectPath(projectId);
	const productionPort = await allocateProjectProductionPort(projectId);

	// Clean up any existing project directory (for idempotent retries)
	await fs.rm(projectPath, { recursive: true, force: true });

	// Ensure projects directory exists
	await fs.mkdir(getProjectsPath(), { recursive: true });

	// Copy template to preview directory
	const previewPath = getProjectPreviewPath(projectId);
	await fs.mkdir(previewPath, { recursive: true });
	await copyTemplate(previewPath);
	logger.debug({ previewPath }, "Copied template to preview directory");

	// Initialize git repo so OpenCode snapshots can track agent file changes
	// (snapshots require a git working tree to compute patches and revert).
	await initGitRepo(previewPath);
	logger.debug({ previewPath }, "Initialized git repo for preview directory");

	// Create production directory
	const productionPath = getProjectProductionPath(projectId);
	await fs.mkdir(productionPath, { recursive: true });
	logger.debug({ productionPath }, "Created production directory");

	// Write .env file with all port configuration
	await writeProjectEnv(projectPath, devPort, projectId, productionPort);
	logger.debug(
		{ devPort, productionPort, projectId },
		"Wrote project .env file",
	);

	// Create logs directories
	await fs.mkdir(path.join(previewPath, "logs"), { recursive: true });
	await fs.mkdir(path.join(productionPath, "logs"), { recursive: true });

	// Generate per-project token used by internal OpenCode tools
	await ensureProjectInternalToken(projectId);

	return { projectPath, productionPort };
}

/**
 * Initialize a git repository in the project directory with an initial commit.
 * Required for OpenCode's snapshot/revert system to track file changes.
 */
async function initGitRepo(repoPath: string): Promise<void> {
	const run = (args: string[]) =>
		new Promise<void>((resolve, reject) => {
			const child = spawn("git", args, {
				cwd: repoPath,
				stdio: "ignore",
				env: {
					...process.env,
					GIT_AUTHOR_NAME: "doce",
					GIT_AUTHOR_EMAIL: "doce@local",
					GIT_COMMITTER_NAME: "doce",
					GIT_COMMITTER_EMAIL: "doce@local",
				},
			});
			child.on("error", reject);
			child.on("exit", (code) =>
				code === 0
					? resolve()
					: reject(new Error(`git ${args.join(" ")} exited with ${code}`)),
			);
		});

	try {
		await run(["init", "-q", "-b", "main"]);
		await run(["add", "-A"]);
		await run(["commit", "-q", "--allow-empty", "-m", "initial template"]);
	} catch (error) {
		logger.warn(
			{ error, repoPath },
			"Failed to initialize git repo for project; revert/snapshot will be disabled",
		);
	}
}

